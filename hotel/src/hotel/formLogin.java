    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Connection kon = koneksi.Koneksi();
        try {
            Statement st = kon.createStatement();
            String sql = "SELECT * FROM login where username ='" + txUser.getText() + "'  and password = '" + txPass.getText() + "'  ";

            ResultSet rs = st.executeQuery(sql);
            if (rs.next()) {
                if (rs.getString("level").equals("1")) {
                    FormAdmin fa = new FormAdmin();
                    fa.show();
                    this.dispose();
                } else if (rs.getString("level").equals("2")) {
                    FormCustomer fa  = new FormCustomer();

                    fa.show();
                    this.dispose();

                }
            } else {
                JOptionPane.showMessageDialog(null,
                    "Maaf password atau username anda salah");
                txUser.setText("");
                txPass.setText("");
                txUser.requestFocus();
            }
        } catch (java.sql.SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

